// Aula 01 - Introdução a variáveis

void main() {
  // Declara e imprime uma variável do tipo String
  String variavelNome = 'Luiz';
  print(variavelNome);

  // Declara e imprime uma variável do tipo inteiro
  int valorVariavel = 10;
  print(valorVariavel);

  // Declara e imprime uma variável booleana
  bool ehVerdadeiro = true;
  print(ehVerdadeiro);

  // Declara e imprime uma lista de Strings
  List<String> listaDePalavras = ['Luiz', 'Fernando'];
  print('${listaDePalavras[0]} - ${listaDePalavras[1]}');
}